package tx.helper.model;

public class CallInfo {
	private String callidentityid;
	private String calltitle;
	private String customername;
	private String brandname;
	private String desc;
	private String address;
	private String state;
	private String engineer;
	private String isactive;
	
	public void setCallidentityid(String Callidentityid) {
		callidentityid = Callidentityid;
	}
	
	public String getCallidentityid() {
		return callidentityid;
	}

	public void setCallTitle(String CallTitle) {
		calltitle = CallTitle;
	}
	
	public String getCallTitle() {
		return calltitle;
	}

	public void setCustomername(String Customername) {
		customername = Customername;
	}
	
	public String getCustomername() {
		return customername;
	}
	
	public void setBrandname(String Brandname) {
		brandname = Brandname;
	}
	
	public String getBrandname() {
		return brandname;
	}
	
	public void setDesc(String Desc) {
		desc = Desc;
	}
	
	public String getDesc() {
		return desc;
	}
	
	public void setAddress(String Address) {
		address = Address;
	}
	
	public String getAddress() {
		return address;
	}	
	
	public void setState(String State) {
		state = State;
	}
	
	public String getState() {
		return state;
	}
	
	public void setEngineer(String Engineer) {
		engineer = Engineer;
	}
	
	public String getEngineer() {
		return engineer;
	}
	
	public void setIsActive(String IsActive) {
		isactive = IsActive;
	}
	
	public String getIsActive() {
		return isactive;
	}
}